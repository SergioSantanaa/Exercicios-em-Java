package br.senai;

public class Empregado {
	private String nome;
	private String cpf;
	private String sobrenome;
	public double vencimento;
	public Empregado(String nome, String cpf, String sobrenome, double vencimento) {
		super();
		this.nome = nome;
		this.cpf = cpf;
		this.sobrenome = sobrenome;
		this.vencimento = vencimento;
	}
	
	
	
}
